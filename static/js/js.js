// Abre la ventana emergente (popup) cambiando su estilo para que se muestre en pantalla
function abrirPopupagregar(){

    document.getElementById("overlayagregar").style.display = "flex";

}

// Cierra la ventana emergente (popup) ocultándola por completo de la pantalla
function cerrarPopupagregar(){

    document.getElementById("overlayagregar").style.display = "none";

}