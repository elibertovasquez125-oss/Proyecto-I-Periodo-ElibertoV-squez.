// Abre la ventana emergente de eliminación cambiando su estilo para que sea visible en pantalla
function abrirPopupeliminar(){

    document.getElementById("overlayeliminar").style.display = "flex";

}

// Cierra la ventana emergente de eliminación ocultándola por completo de la pantalla
function cerrarPopupeliminar(){

    document.getElementById("overlayeliminar").style.display = "none";

}