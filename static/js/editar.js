// Abre la ventana emergente de edición cambiando su estilo para que sea visible en pantalla
function abrirPopupeditar(){
    document.getElementById("overlayeditar").style.display = "flex";

}

// Cierra la ventana emergente de edición ocultándola por completo de la pantalla
function cerrarPopupeditar(){

    document.getElementById("overlayeditar").style.display = "none";

}